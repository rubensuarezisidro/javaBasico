
package clases.clase3.centros;


public class Medico {
    private int numLicencia;
    private String nomMedico;
    private int aaGraduado;

    public int getNumLicencia() {
        return numLicencia;
    }

    public void setNumLicencia(int numLicencia) {
        this.numLicencia = numLicencia;
    }

    public String getNomMedico() {
        return nomMedico;
    }

    public void setNomMedico(String nomMedico) {
        this.nomMedico = nomMedico;
    }

    public int getAaGraduado() {
        return aaGraduado;
    }

    public void setAaGraduado(int aaGraduado) {
        this.aaGraduado = aaGraduado;
    }
    
    
}
